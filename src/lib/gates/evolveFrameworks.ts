// ── GATE 3: MARKET SOPHISTICATION FRAMEWORK ──────────────

export const EVOLVE_MARKET_SOPHISTICATION = `
=== EVOLVE MARKET SOPHISTICATION FRAMEWORK ===

THE 5 STAGES OF MARKET SOPHISTICATION (Eugene Schwartz):
Stage 1: FIRST TO MARKET — Simple benefit claims work. "Buy this, it works."
Stage 2: COMPETITION EMERGES — Enlarge the claim. "Ours is 10x stronger."
Stage 3: MARKET SATURATED — Claims no longer believed. Need NEW MECHANISM. "Here's a completely new WAY it works."
Stage 4: MECHANISMS COPIED — Enlarge the mechanism. "Our mechanism is patented/proprietary."
Stage 5: COMPLETE EXHAUSTION — Only identity/emotion works. "This is WHO you become."

CRITICAL: Most e-commerce markets operate in stages 3-5. Markets now sophisticate in MONTHS (not years) due to social media/TikTok/Reddit.

YOUR TASK: Assess the market's sophistication stage BEFORE building the mechanism.

HOW TO ASSESS:
1. Analyze product mechanisms across competitors (what are they claiming?)
2. Look at competitor ad copy (are they still making simple claims or using mechanisms?)
3. Look at other solutions to the same desire (how educated is the market?)
4. Check customer language (are they skeptical? Do they compare mechanisms?)

THREE STRATEGIC RESPONSES FOR STAGES 3-5:

1. NEW MECHANISM — Revolutionary delivery/technology
   - Introduces a NEW process, technology, or delivery system
   - Makes familiar promises fresh and believable
   - "These new mechanisms give people NEW HOPE that this product will solve their problem"
   - S-T-O-K Framework: Situation (market context) → Task (what to find) → Objective (new hope) → Knowledge (product features)
   - Look for: new delivery methods, innovative approaches, uncommon ingredients, specific combinations, contrarian approaches

2. NEW INFORMATION — Education as competitive advantage
   - Reframe product understanding through consumer education
   - Reveal problems customers didn't know existed
   - Must be recent (last 12-24 months), verifiable, not yet widely used in marketing
   - Position brand as AUTHORITY through education
   - Example: "Big shroom companies are hiding this from you" → educates on digestion killing benefits → introduces buccal mechanism

3. NEW IDENTITY — Lifestyle movements and tribal belonging (ADVANCED)
   - Creates emotional connections through lifestyle movements
   - WHO customers become > what products do
   - Requires years of brand-building (Liquid Death, Glossier, Peloton)
   - For most brands: "new identity" = finding underserved avatars to market to
`;

// ── GATE 4: HOOK & COPY EXECUTION FRAMEWORK ─────────────

export const EVOLVE_EXECUTION_FRAMEWORK = `
=== EVOLVE AD EXECUTION FRAMEWORK ===

THE 7 REASONS ADS FAIL (use as quality checklist):
1. COPY LACKS EMOTIONAL IMPACT — "Lose weight" (zero) vs "Lose the muffin top for good" (visual, specific)
2. NO CURIOSITY CREATED — "I accidentally lost 12 pounds at 52" = massive curiosity
3. NO SUSPENSE — Once hook works, no reason to CONTINUE. Must promise a payoff.
4. TOO COMPLEX — Above 6th grade reading level = people don't follow. Use Hemingway App.
5. TELL, DON'T SHOW — "Feel more energized" = TELL. "Go the whole day without a Monster" = SHOW.
6. OBJECTIONS NOT ADDRESSED — Framework: Objection → Claim → Proof → Benefit. Cycle as needed.
7. SKIPPING AWARENESS LEVELS — Problem Aware jumping to Most Aware skips Solution Aware + Product Aware.

CRITICAL RULE: Every ad must carry the conversation through ALL remaining awareness stages.

3 HOOK REQUIREMENTS (every hook MUST do all 3):
1. Call the RIGHT audience (not everyone)
2. Promise a BENEFIT
3. Create CURIOSITY to keep reading/watching

4 VISUAL HOOK TYPES (for video ads):
1. Clip showing IDEAL OUTCOME
2. WEIRD/unexpected clip (pattern interrupt)
3. Clip showing the PROBLEM
4. Clip showing the PRODUCT

HOOK × AWARENESS MATRIX:
- Unaware: Outcome clip OR Problem clip
- Problem Aware: Problem clip OR Solution clip
- Solution Aware: Solution clip OR Outcome clip
- Product Aware: Solution clip OR Product clip
- Most Aware: Product clip / promo banner

LOWER VALENCE HOOKS:
Instead of selling the positive result, tap into a SLIGHTLY negative emotion: "there might be a problem you're not aware of." Creates SUSPENSE + URGENCY.
Example: "If 1,200 calories feels like torture, your fullness hormone is likely being destroyed."

JUXTAPOSITION HOOKS:
Combine two things that don't normally go together.
Example: "1200 calories is easy" (calorie counting + ease = unexpected)
Example: "Dog teeth cleaning takes 60 seconds" (dental care + speed = unexpected)

OBJECTION/CLAIM/PROOF/BENEFIT CYCLE:
- Objection: "I've tried supplements before and they didn't work"
- Claim: "This isn't another appetite suppressant. It blocks the enzyme destroying your appetite hormone"
- Proof: "In a 12-week study, women ate 24% fewer calories in week one"
- Benefit: "Imagine eating 1200 calories and feeling satisfied, not deprived"
Cycle this pattern as many times as needed in the ad.

LAW OF UNIQUE NUMBERS: 12 pounds (not 10), 52 years (not 50), 56.4% (not 60%). Irregular numbers = more believable.

SHOW DON'T TELL: Transform EVERY claim into a mental image. (Full audit framework with examples → see ZAK Quality Gates.)

CREATIVE BENCHMARKS:
| Metric | OK | Good | Very Good |
| Hook Rate | 21% | 30% | 40%+ |
| Hold Rate | 5% | 10% | 15%+ |
| CTR | 0.6% | 1%+ | 1.5%+ |
`;

